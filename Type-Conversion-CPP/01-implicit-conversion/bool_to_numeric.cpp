#include <iostream>

int main()
{
    bool a = 1;     // true
    bool b = 0;     // false
    bool c = 'g';   // true
    bool d = 3.4;   // true
    
    std::cout << "a = " << a << std::endl;
    std::cout << "b = " << b << std::endl;
    std::cout << "c = " << c << std::endl;
    std::cout << "d = " << d << std::endl;
    return 0;
}
